
public class FloatMultiplication {

	public static void main(String[] args) {
	
		double num1 = 10.20;
		double num2 = 530.20;
		double result;
		
		result=num1*num2;
		System.out.print("Result of multiplcation is " + result);
		
		
		
		float num3 = 10.20f;
		float num4 = 530.20f;
		float result1;
		
		result1=num3*num4;
		System.out.print("Result of multiplcation is " + result1);
		

		

	}

}
