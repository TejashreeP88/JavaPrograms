package Programs;

public class SwapNumbers {

	public static void main(String[] args) {
		int a=289;
		int b=343;
		int temp;
		
		temp=a;
		a=b;
		b=temp;
		
		System.out.println(a + "and" + b);
		
		
		
		int c=948;
		int d=904;
	
		
		c=c+d;
		d=c-d;
		c=c-d;
		
		System.out.println(c + "and" + d);

	}

}
